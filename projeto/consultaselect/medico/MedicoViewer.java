    private void realizarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_realizarConsultaActionPerformed
        // TODO add your handling code here:
        if(filterButton.getSelectedItem().equals("Todos")){
            infoTextArea.setText("");
            ArrayList<Medico> a = MedicoController.getAllMedicos();
            for(Medico m : a){
                infoTextArea.append("ID: " + m.getId() + "\nNOME: " + m.getNome() + "\nESPECIALIDADE: " + m.getEspecialidade() + "\nCRO: " + m.getCro() + "\n=======================================\n");
            }
        } else if(filterButton.getSelectedItem().equals("Especialidade")){
            infoTextArea.setText("");
            String especialidade = especialidadeTextField.getText();
            ArrayList<Medico> b = MedicoController.getAllMedicos();
            for(Medico m : b){
                if(m.getEspecialidade().equals(especialidade)){
                    infoTextArea.append("ID: " + m.getId() + "\nNOME: " + m.getNome() + "\nESPECIALIDADE: " + m.getEspecialidade() + "\nCRO: " + m.getCro() + "\n=======================================\n");
                }
                
            }
        }
    }//GEN-LAST:event_realizarConsultaActionPerformed

